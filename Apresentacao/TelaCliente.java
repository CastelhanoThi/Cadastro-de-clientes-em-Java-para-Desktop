    private void jBbuscarCPFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBbuscarCPFActionPerformed
        con.conectar();
        String buscar_cpf = "select * from tb_clientes where cpf='" + jTcpf.getText() + "';";

        try {
            c = cliDAO.buscar_cpf(buscar_cpf);
            
            if (jTcpf.getText().length() == 0) {
                jLvazio.setText("Campo vazio tente novamente");
            }else if(jTcpf.getText().equals(c.getCpf())){
                jTcodigo.setText(String.valueOf(c.getId_clientes()));
                jTnome.setText(c.getNome());
                jTendereco.setText(c.getEndereco());
                jTemail.setText(c.getEmail());
                jTcidade.setText(c.getCidade());
                jTbairro.setText(c.getBairro());
                jFcpf.setText(c.getCpf());
                jFrg.setText(c.getRg());
                jCuf.setSelectedItem(c.getUf());
            }else{
                jLresult.setText("CPF nao encontrado");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jBbuscarCPFActionPerformed

